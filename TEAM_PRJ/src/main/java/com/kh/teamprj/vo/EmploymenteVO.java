package com.kh.teamprj.vo;

public class EmploymenteVO {
    private String emplNo;
	private String isYn;
	private String emplYear;
	private String stuNo;
	private double num;
	
	public String getEmplNo() {
		return emplNo;
	}
	public void setEmplNo(String emplNo) {
		this.emplNo = emplNo;
	}
	public String getIsYn() {
		return isYn;
	}
	public void setIsYn(String isYn) {
		this.isYn = isYn;
	}
	public String getEmplYear() {
		return emplYear;
	}
	public void setEmplYear(String emplYear) {
		this.emplYear = emplYear;
	}
	public String getStuNo() {
		return stuNo;
	}
	public void setStuNo(String stuNo) {
		this.stuNo = stuNo;
	}
	public double getNum() {
		return num;
	}
	public void setNum(double num) {
		this.num = num;
	}
	
	
}
